package com.leetcode.questions;

import org.junit.Before;
import org.junit.Test;

import static org.assertj.core.api.Assertions.assertThat;

public class ValidSudokuTest {
    private ValidSudoku validSudoku;

    @Before
    public void setUp() {
        validSudoku = new ValidSudoku();
    }

    @Test
    public void test_1() {
        char[][] board = new char[][]{
                {'5', '3', '.', '.', '7', '.', '.', '.', '.'},
                {'6', '.', '.', '1', '9', '5', '.', '.', '.'},
                {'.', '9', '8', '.', '.', '.', '.', '6', '.'},
                {'8', '.', '.', '.', '6', '.', '.', '.', '3'},
                {'4', '.', '.', '8', '.', '3', '.', '.', '1'},
                {'7', '.', '.', '.', '2', '.', '.', '.', '6'},
                {'.', '6', '.', '.', '.', '.', '2', '8', '.'},
                {'.', '.', '.', '4', '1', '9', '.', '.', '5'},
                {'.', '.', '.', '.', '8', '.', '.', '7', '9'}
        };

        assertThat(validSudoku.isValidSudoku(board)).isTrue();
    }

    @Test
    public void test_2() {
        char[][] board = new char[][]{
                {'8', '3', '.', '.', '7', '.', '.', '.', '.'},
                {'6', '.', '.', '1', '9', '5', '.', '.', '.'},
                {'.', '9', '8', '.', '.', '.', '.', '6', '.'},
                {'8', '.', '.', '.', '6', '.', '.', '.', '3'},
                {'4', '.', '.', '8', '.', '3', '.', '.', '1'},
                {'7', '.', '.', '.', '2', '.', '.', '.', '6'},
                {'.', '6', '.', '.', '.', '.', '2', '8', '.'},
                {'.', '.', '.', '4', '1', '9', '.', '.', '5'},
                {'.', '.', '.', '.', '8', '.', '.', '7', '9'}
        };

        assertThat(validSudoku.isValidSudoku(board)).isFalse();
    }

    @Test
    public void test_3() {
        char[][] board = new char[][]{
                {'.', '.', '5', '.', '.', '.', '.', '.', '.'},
                {'1', '.', '.', '2', '.', '.', '.', '.', '.'},
                {'.', '.', '6', '.', '.', '3', '.', '.', '.'},
                {'8', '.', '.', '.', '.', '.', '.', '.', '.'},
                {'3', '.', '1', '5', '2', '.', '.', '.', '.'},
                {'.', '.', '.', '.', '.', '.', '.', '4', '.'},
                {'.', '.', '6', '.', '.', '.', '.', '.', '.'},
                {'.', '.', '.', '.', '.', '.', '.', '9', '.'},
                {'.', '.', '.', '.', '.', '.', '.', '.', '.'}
        };

        assertThat(validSudoku.isValidSudoku(board)).isFalse();
    }
}